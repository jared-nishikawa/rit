def red(txt):
    return "\x1b[31m" + txt + "\x1b[0m"

def green(txt):
    return "\x1b[32m" + txt + "\x1b[0m"

def yellow(txt):
    return "\x1b[33m" + txt + "\x1b[0m"

def blue(txt):
    return "\x1b[34m" + txt + "\x1b[0m"

def magenta(txt):
    return "\x1b[35m" + txt + "\x1b[0m"

def cyan(txt):
    return "\x1b[36m" + txt + "\x1b[0m"
